public class NotifyMessage {

    private String out_order_no;
    private String uuid;
    private String merchant_address;

    private String type;

    private String amount;

    private String amount_hex;

    private String got_amount;

    private String pay_result;

    private String bind_uuid;

    private String user_id;

    private String token;

    private String sign;

    public String getOut_order_no() {
        return out_order_no;
    }

    public void setOut_order_no(String out_order_no) {
        this.out_order_no = out_order_no;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getMerchant_address() {
        return merchant_address;
    }

    public void setMerchant_address(String merchant_address) {
        this.merchant_address = merchant_address;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getAmount_hex() {
        return amount_hex;
    }

    public void setAmount_hex(String amount_hex) {
        this.amount_hex = amount_hex;
    }

    public String getGot_amount() {
        return got_amount;
    }

    public void setGot_amount(String got_amount) {
        this.got_amount = got_amount;
    }

    public String getPay_result() {
        return pay_result;
    }

    public void setPay_result(String pay_result) {
        this.pay_result = pay_result;
    }

    public String getBind_uuid() {
        return bind_uuid;
    }

    public void setBind_uuid(String bind_uuid) {
        this.bind_uuid = bind_uuid;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
}
